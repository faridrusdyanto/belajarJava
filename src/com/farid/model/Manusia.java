package com.farid.model;

public class Manusia {
	private String mata;
	private String tangan;
	private String kaki;
	
	public void setMata(String mata) {
		this.mata = mata;
		
	}
	public String getMata() {
		return this.mata;
	}
	public String getTangan() {
		return tangan;
	}
	public void setTangan(String tangan) {
		this.tangan = tangan;
	}
	public String getKaki() {
		return kaki;
	}
	public void setKaki(String kaki) {
		this.kaki = kaki;
	}
	

}
