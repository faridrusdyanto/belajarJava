package com.farid.oop;

public class Motor {
	private Long mesin;
	private Integer roda;
	private Integer karbu;
	private String stang;
	private Boolean rem;
	private Integer tangki;
	
	public void setMesin(Long mesin) {
		this.mesin = mesin;
	}
	public Long getMesin() {
		return this.mesin;
	}
	public void setRoda(Integer roda) {
		this.roda = roda;
	}
	public Integer getRoda() {
		return this.roda;
	}
	public Integer getKarbu() {
		return karbu;
	}
	public void setKarbu(Integer karbu) {
		this.karbu = karbu;
	}
	public String getStang() {
		return stang;
	}
	public void setStang(String stang) {
		this.stang = stang;
	}
	public Boolean getRem() {
		return rem;
	}
	public void setRem(Boolean rem) {
		this.rem = rem;
	}
	public Integer getTangki() {
		return tangki;
	}
	public void setTangki(Integer tangki) {
		this.tangki = tangki;
	}
	
	
	
}

